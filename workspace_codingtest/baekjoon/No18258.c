#include <stdio.h>
#include <string.h>

int number[100001] = {0};
int cnt = 0;

void push(int num) {
    number[cnt] = num;
    cnt++;
}

void pop() {
    if(cnt != 0) {

        printf("%d\n", number[0]);

        for(int i = 0 ; i < cnt; i++) {
            number[i] = number[i + 1];
        }

        cnt--;

    } else {

        printf("%d\n", -1);

    }

}

void size() {
    printf("%d\n", cnt);
}

void empty() {
    if(cnt == 0) {
        printf("%d\n", 1);
    } else {
        printf("%d\n", 0);
    }
}

void front() {
    if(cnt != 0) {
        printf("%d\n", number[0]);
    } else {
        printf("%d\n", -1);
    }
}

void back() {
    if(cnt != 0) {
        printf("%d\n", number[cnt - 1]);
    } else {
        printf("%d\n", -1);
    }
}

int main(void) {
    int N = 0;
    scanf("%d", &N);

    char stack[10] = {0};
    int num = 0;

    for(int i = 0; i < N; i++) {
        scanf("%s", stack);

        if(strcmp(stack, "push") == 0) {
            scanf("%d", &num);

            push(num);
        } else if(strcmp(stack, "pop") == 0) {
            pop();
        } else if(strcmp(stack, "size") == 0) {
            size();
        } else if(strcmp(stack, "empty") == 0) {
            empty();
        } else if(strcmp(stack, "front") == 0) {
            front();
        } else if(strcmp(stack, "back") == 0) {
            back();
        }
    }

    return 0;
}